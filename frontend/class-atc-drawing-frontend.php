namespace atcDrawing\frontend;

use atcDrawing as NS;
use atcDrawing\includes as Includes;

class Atc_Drawing_Frontend
{

    /**
     * The ID of this plugin.
     *
     * @since    1.0.0
     * @access   private
     * @var      string    $plugin_name    The ID of this plugin.
     */
    private $plugin_name;

    /**
     * The version of this plugin.
     *
     * @since    1.0.0
     * @access   private
     * @var      string    $version    The current version of this plugin.
     */
    private $version;

    /**
     * The user capability needed to use this plugin.
     *
     * @since    1.0.0
     * @access   private
     * @var      string    $capability    The user capability needed to use this plugin.
     */
    private $capability;

    /**
     * Initialize the class and set its properties.
     *
     * @since    1.0.0
     * @param      string    $plugin_name       The name of this plugin.
     * @param      string    $version    The version of this plugin.
     */
    public function __construct($plugin_name, $version)
    {
        $this->plugin_name = $plugin_name;
        $this->version = $version;
        $this->capability = 'manage_options';
    }
}
